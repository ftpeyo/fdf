#include "libft.h"

int	ft_intlen(int nbr)
{
	int	ret;

	ret = 1;
	if (nbr < 0)
	{
		nbr *= -1;
		ret++;
	}
	while (nbr /= 10)
		ret++;
	return (ret);
}
